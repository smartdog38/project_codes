# 解包（以元组为例）
# 基本解包
a = (1, 2, 3, 4)
print(*a)  # 1 2 3 4
# 解包到变量
a = (1, 2, 3, 4)
x, y, *z = a
print(x, y, z)  # 1 2 [3, 4]，剩余元素放进列表
# 解包嵌套元组
a = (1, 2, 3, 4, (6, 7, 8))
x, y, *z = a
print(x, y, z)  # 1 2 [3, 4, (6, 7, 8)]
# 解包多个嵌套元组
a = (1, 2, 3, 4, (6, 7, 8), (9, 10, 11))
x, y, *z = a
print(x, y, z)  # 1 2 [3, 4, (6, 7, 8), (9, 10, 11)]
# 解包时保留嵌套结构
a = (1, 2, (6, 7, 8))
x, y, *z = a
print(x, y, z)  # 1 2 [(6, 7, 8)]
# 多重解包
a = (1, 2,(6, 7, 8))
x1,y1,z1 = a
x2,y2,z2 =z1
print(x1,y1,z1,x2,y2,z2) # 1 2 [(6, 7, 8)] 6 7 [8]